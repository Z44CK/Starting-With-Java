public class atribuicao {
    public static void main(String[] args) {
        int n1 = 1;
        int n2 = 2;

        boolean simNao = n1 == n2;

        System.out.println("Numero Um, é igual ao numero dois? " + simNao);

        simNao = n1 != n2;

        System.out.println("Numero Um, é diferente do numero dois?" + simNao);

    }
}
